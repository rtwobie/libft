#include <stddef.h>
#include "libft.h"

size_t	ft_strlcpy(char	*dest, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (!size)
		return (ft_strlen(src));
	while (src[i] && i + 1 < size)
		*dest++ = src[i++];
	*dest = '\0';
	return (ft_strlen(src));
}
